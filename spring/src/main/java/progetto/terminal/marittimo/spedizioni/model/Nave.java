package progetto.terminal.marittimo.spedizioni.model;

public class Nave {
    private int id;
    private String nome;

    public Nave(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}